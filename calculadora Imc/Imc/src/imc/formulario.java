    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
peso = Float.parseFloat(jPeso.getText());  
altura = Float.parseFloat(jAltura.getText());

imc = peso / (altura * altura);

lblResultado.setText(String.valueOf(imc));

if (imc <= 18.5) {
    jMensagem.setText("Abaixo do peso");
} else if (imc >= 18.5 && imc <= 24.9) {
    jMensagem.setText("Peso ideal");
} else if (imc >= 25.0 && imc <= 29.9) {
    jMensagem.setText("Levemente acima do peso");
} else if (imc >= 30.0 && imc <= 34.9) {
    jMensagem.setText("Obesidade grau I");
} else if (imc >= 35.0 && imc <= 39.9) {
    jMensagem.setText("Obesidade grau II (severa) ");
}
else if (imc >= 40.0 ) {
    jMensagem.setText("Obesidade grau III (m√≥rbida) ");
}
    }//GEN-LAST:event_btnCalcularActionPerformed

